"""Gemini prompts."""

# ruff: noqa: RUF001  # кириллица в промптах намеренно


def default_prompt(topic: str | None = None) -> str:
    theme = (
        topic.strip() if (topic and topic.strip()) else
        "разработка, языки и инструменты, карьера, советы, факты из IT"
    )
    return f"""Ты автор постов для Telegram-канала о программировании.
Пиши короткие, цепляющие посты на тему: {theme}.

Формат разметки — только тот, что поддерживает Telegram:
*жирный* — одна звёздочка с двух сторон
_курсив_ — одно подчёркивание с двух сторон
`код` — обратные кавычки для кода и команд
[текст ссылки](url) — ссылки в таком виде

Не используй: **, __, ##, ###, списки с - или *, блоки кода с ```.
Пиши живым языком, без клише. Не добавляй призывы подписаться и хэштеги.

Сгенерируй один пост. Верни только текст поста, без кавычек и пояснений."""
